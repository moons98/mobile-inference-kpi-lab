import pandas as pd
import numpy as np
from pathlib import Path
from typing import Optional, Tuple
from dataclasses import dataclass


@dataclass
class ColdStartMetrics:
    """Cold start timing metrics."""
    model_load_ms: float
    session_create_ms: float  # QNN compilation time
    first_inference_ms: float
    total_cold_ms: float


@dataclass
class KpiMetrics:
    """Aggregated KPI metrics from a benchmark run."""
    # Latency metrics (total E2E)
    latency_p50: float
    latency_p95: float
    latency_mean: float
    latency_std: float
    latency_min: float
    latency_max: float
    inference_count: int

    # E2E breakdown (P50)
    preprocess_p50: float       # Image preprocessing P50
    inference_only_p50: float   # Model inference only P50
    input_create_p50: float     # Input tensor creation (JNI boundary)
    output_copy_p50: float      # Output tensor copy (native → JVM)
    postprocess_p50: float      # NMS + coordinate transform P50
    detection_count_mean: float # Average detection count per frame

    # Throughput
    fps: float  # Inferences per second

    # Thermal drift metrics (for sustained performance analysis)
    first_30s_p50: float  # E2E Latency p50 in first 30 seconds
    last_30s_p50: float   # E2E Latency p50 in last 30 seconds
    drift_percent: float  # (last - first) / first * 100

    # Inference-only drift (session.run sustained behavior)
    infer_first_30s_p50: float  # Inference-only P50 in first 30 seconds
    infer_last_30s_p50: float   # Inference-only P50 in last 30 seconds
    infer_drift_percent: float  # (last - first) / first * 100

    # Thermal metrics
    thermal_start: float
    thermal_end: float
    thermal_max: float
    thermal_slope: float  # °C per minute

    # Power metrics
    power_mean: float
    power_std: float

    # Memory metrics
    memory_peak: int
    memory_mean: float
    memory_min: int
    memory_start: int    # first sample
    memory_end: int      # last sample

    # Duration
    duration_seconds: float

    # Cold start (optional, from metadata)
    cold_start: Optional[ColdStartMetrics] = None

    # ORT Profiling: NPU compute vs framework overhead (optional, from metadata)
    profiling_iterations: int = 0
    profiling_avg_total_ms: float = 0.0
    profiling_avg_npu_ms: float = 0.0
    profiling_avg_cpu_ms: float = 0.0
    profiling_avg_fence_ms: float = 0.0
    profiling_avg_overhead_ms: float = 0.0

    # Pipeline benchmark (optional, from metadata)
    pipeline_sequential_fps: float = 0.0
    pipeline_pipelined_fps: float = 0.0
    pipeline_speedup: float = 0.0
    pipeline_sequential_ms: float = 0.0
    pipeline_pipelined_ms: float = 0.0


def load_metadata(file_path: str) -> dict:
    """
    Extract metadata from comment lines in a KPI log CSV file.

    Args:
        file_path: Path to the CSV file

    Returns:
        Dict with metadata key-value pairs
    """
    metadata = {}
    with open(file_path, 'r', encoding='utf-8') as f:
        for line in f:
            if line.startswith('# '):
                parts = line[2:].strip().split(',', 1)
                if len(parts) == 2:
                    metadata[parts[0]] = parts[1]
            elif not line.startswith('#'):
                break  # Stop at data rows
    return metadata


def load_ort_log(csv_path: str) -> Optional[dict]:
    """
    Load ORT log file if it exists alongside CSV file.

    Args:
        csv_path: Path to the CSV file

    Returns:
        Dict with ORT log info or None if not found
    """
    import re

    # ORT log file has same name as CSV but with _ort.log suffix
    # e.g., kpi_YOLOv8n_QNNNPU_20260130_173916.csv -> kpi_YOLOv8n_QNNNPU_20260130_173916_ort.log
    log_path = Path(str(csv_path).replace('.csv', '_ort.log'))

    if not log_path.exists():
        return None

    try:
        with open(log_path, 'r', encoding='utf-8') as f:
            content = f.read()

        ort_info = {
            'raw_log': content,
            'total_nodes': 0,
            'qnn_nodes': 0,
            'cpu_nodes': 0,
            'fallback_ops': [],
            'qnn_error': None
        }

        # Parse summary section (header generated by app)
        for line in content.split('\n'):
            if 'Total nodes:' in line:
                try:
                    ort_info['total_nodes'] = int(line.split(':')[1].strip())
                except:
                    pass
            elif 'QNN nodes:' in line:
                try:
                    ort_info['qnn_nodes'] = int(line.split(':')[1].strip())
                except:
                    pass
            elif 'CPU fallback nodes:' in line:
                try:
                    ort_info['cpu_nodes'] = int(line.split(':')[1].strip())
                except:
                    pass
            elif 'Fallback ops:' in line:
                ops_str = line.split(':')[1].strip()
                if ops_str:
                    ort_info['fallback_ops'] = [op.strip() for op in ops_str.split(',')]

        # If summary has zero total nodes, parse from raw logs
        if ort_info['total_nodes'] == 0:
            # Reset counts to parse from raw logs
            ort_info['qnn_nodes'] = 0
            ort_info['cpu_nodes'] = 0

            for line in content.split('\n'):
                # Format A: "All nodes placed on [QNNExecutionProvider]. Number of nodes: 77"
                match = re.search(r'nodes placed on \[(\w+)\].*Number of nodes:\s*(\d+)', line)
                if match:
                    ep_name = match.group(1)
                    node_count = int(match.group(2))
                    ort_info['total_nodes'] += node_count
                    if 'QNN' in ep_name:
                        ort_info['qnn_nodes'] += node_count
                    elif 'CPU' in ep_name:
                        ort_info['cpu_nodes'] += node_count
                    continue

                # Format B (ORT 1.x): "GetCapability] Number of partitions supported by QNN EP: 1,
                #   number of nodes in the graph: 233, number of nodes supported by QNN: 233"
                # ORT calls GetCapability multiple times (per optimization pass); use the last value.
                match = re.search(
                    r'GetCapability.*number of nodes in the graph:\s*(\d+).*number of nodes supported by QNN:\s*(\d+)',
                    line, re.IGNORECASE
                )
                if match:
                    graph_count = int(match.group(1))
                    qnn_count = int(match.group(2))
                    ort_info['total_nodes'] = graph_count
                    ort_info['qnn_nodes'] = qnn_count
                    ort_info['cpu_nodes'] = graph_count - qnn_count

        # Parse fallback ops from raw logs
        # Example: "Validation FAILED for 1 nodes in NodeUnit (QuantizeLinear) :"
        for line in content.split('\n'):
            match = re.search(r'Validation FAILED.*NodeUnit\s*\(([^)]+)\)', line)
            if match:
                op = match.group(1)
                if op not in ort_info['fallback_ops']:
                    ort_info['fallback_ops'].append(op)

        # Always check for QNN errors in raw logs
        for line in content.split('\n'):
            if 'SetupBackend failed' in line or 'QNN_DEVICE_ERROR' in line or 'QNN_COMMON_ERROR' in line:
                error_match = re.search(r'Error:\s*([^,\n]+)', line)
                if error_match:
                    ort_info['qnn_error'] = error_match.group(1).strip()
                    break  # Only need first error

        return ort_info
    except Exception as e:
        print(f"Warning: Failed to load ORT log {log_path}: {e}")
        return None


def load_log(file_path: str) -> pd.DataFrame:
    """
    Load a KPI log CSV file.

    Args:
        file_path: Path to the CSV file

    Returns:
        DataFrame with parsed log data
    """
    # Skip comment lines starting with '#' (metadata)
    df = pd.read_csv(file_path, comment='#')

    # Convert timestamp to datetime
    df['datetime'] = pd.to_datetime(df['timestamp'], unit='ms')

    # Calculate relative time in seconds
    start_time = df['timestamp'].min()
    df['elapsed_seconds'] = (df['timestamp'] - start_time) / 1000.0

    return df


def split_events(df: pd.DataFrame) -> Tuple[pd.DataFrame, pd.DataFrame]:
    """
    Split log into inference and system events.

    Args:
        df: Full log DataFrame

    Returns:
        Tuple of (inference_df, system_df)
    """
    inference_df = df[df['event_type'] == 'INFERENCE'].copy()
    system_df = df[df['event_type'] == 'SYSTEM'].copy()

    return inference_df, system_df


def calculate_metrics(df: pd.DataFrame, metadata: dict = None) -> KpiMetrics:
    """
    Calculate aggregated KPI metrics from a log file.

    Args:
        df: Full log DataFrame
        metadata: Optional metadata dict from CSV header

    Returns:
        KpiMetrics with calculated values
    """
    inference_df, system_df = split_events(df)

    # Latency metrics
    latencies = inference_df['latency_ms'].dropna()

    # Duration
    duration = df['elapsed_seconds'].max()

    # Throughput (FPS)
    fps = len(latencies) / duration if duration > 0 else 0

    # First 30s and Last 30s latency p50 (for thermal drift analysis)
    first_30s_latencies = inference_df[inference_df['elapsed_seconds'] <= 30]['latency_ms'].dropna()
    last_30s_latencies = inference_df[inference_df['elapsed_seconds'] >= duration - 30]['latency_ms'].dropna()

    first_30s_p50 = first_30s_latencies.quantile(0.50) if len(first_30s_latencies) > 0 else 0
    last_30s_p50 = last_30s_latencies.quantile(0.50) if len(last_30s_latencies) > 0 else 0

    # Inference-only drift (session.run sustained behavior)
    infer_first_30s_p50 = 0.0
    infer_last_30s_p50 = 0.0
    infer_drift_percent = 0.0
    if 'inference_ms' in inference_df.columns:
        first_30s_infer = inference_df[inference_df['elapsed_seconds'] <= 30]['inference_ms'].dropna()
        last_30s_infer = inference_df[inference_df['elapsed_seconds'] >= duration - 30]['inference_ms'].dropna()
        infer_first_30s_p50 = first_30s_infer.quantile(0.50) if len(first_30s_infer) > 0 else 0
        infer_last_30s_p50 = last_30s_infer.quantile(0.50) if len(last_30s_infer) > 0 else 0
        if infer_first_30s_p50 > 0:
            infer_drift_percent = (infer_last_30s_p50 - infer_first_30s_p50) / infer_first_30s_p50 * 100

    # Thermal metrics
    thermals = system_df['thermal_c'].dropna()
    thermal_times = system_df.loc[thermals.index, 'elapsed_seconds']

    # Calculate thermal slope (°C per minute)
    if len(thermals) >= 2:
        # Linear regression
        coeffs = np.polyfit(thermal_times / 60.0, thermals, 1)
        thermal_slope = coeffs[0]
    else:
        thermal_slope = 0.0

    # Power metrics
    powers = system_df['power_mw'].dropna()

    # Memory metrics
    memories = system_df['memory_mb'].dropna()

    # Cold start metrics from metadata
    cold_start = None
    if metadata:
        try:
            model_load = float(metadata.get('model_load_ms', 0))
            session_create = float(metadata.get('session_create_ms', 0))
            first_inf_str = metadata.get('first_inference_ms', 'N/A')
            first_inf = float(first_inf_str) if first_inf_str != 'N/A' else 0
            total_cold = float(metadata.get('total_cold_ms', 0))

            if model_load > 0 or session_create > 0:
                cold_start = ColdStartMetrics(
                    model_load_ms=model_load,
                    session_create_ms=session_create,
                    first_inference_ms=first_inf,
                    total_cold_ms=total_cold
                )
        except (ValueError, TypeError):
            pass

    # E2E breakdown metrics (handle old CSV format without these columns)
    has_breakdown = 'inference_ms' in inference_df.columns
    if has_breakdown:
        preprocess_times = inference_df['preprocess_ms'].dropna()
        inference_only_times = inference_df['inference_ms'].dropna()
        postprocess_times = inference_df['postprocess_ms'].dropna()
        detection_counts = inference_df['detection_count'].dropna()

        preprocess_p50 = preprocess_times.quantile(0.50) if len(preprocess_times) > 0 else 0
        inference_only_p50 = inference_only_times.quantile(0.50) if len(inference_only_times) > 0 else 0
        postprocess_p50 = postprocess_times.quantile(0.50) if len(postprocess_times) > 0 else 0
        detection_count_mean = detection_counts.mean() if len(detection_counts) > 0 else 0
    else:
        preprocess_p50 = 0
        inference_only_p50 = latencies.quantile(0.50) if len(latencies) > 0 else 0
        postprocess_p50 = 0
        detection_count_mean = 0

    # Boundary timing (new columns, may not exist in old CSVs)
    has_boundary = 'input_create_ms' in inference_df.columns
    if has_boundary:
        input_create_times = inference_df['input_create_ms'].dropna()
        output_copy_times = inference_df['output_copy_ms'].dropna()
        input_create_p50 = input_create_times.quantile(0.50) if len(input_create_times) > 0 else 0
        output_copy_p50 = output_copy_times.quantile(0.50) if len(output_copy_times) > 0 else 0
    else:
        input_create_p50 = 0
        output_copy_p50 = 0

    # Drift percent
    drift_percent = 0.0
    if first_30s_p50 > 0:
        drift_percent = (last_30s_p50 - first_30s_p50) / first_30s_p50 * 100

    # ORT profiling breakdown (from metadata)
    profiling_iterations = 0
    profiling_avg_total_ms = 0.0
    profiling_avg_npu_ms = 0.0
    profiling_avg_cpu_ms = 0.0
    profiling_avg_fence_ms = 0.0
    profiling_avg_overhead_ms = 0.0
    if metadata:
        try:
            profiling_iterations = int(metadata.get('profiling_iterations', 0))
            if profiling_iterations > 0:
                profiling_avg_total_ms = float(metadata.get('profiling_avg_total_ms', 0))
                profiling_avg_npu_ms = float(metadata.get('profiling_avg_npu_ms', 0))
                profiling_avg_cpu_ms = float(metadata.get('profiling_avg_cpu_ms', 0))
                profiling_avg_fence_ms = float(metadata.get('profiling_avg_fence_ms', 0))
                profiling_avg_overhead_ms = float(metadata.get('profiling_avg_overhead_ms', 0))
        except (ValueError, TypeError):
            pass

    # Pipeline benchmark results (from metadata)
    pipeline_sequential_fps = 0.0
    pipeline_pipelined_fps = 0.0
    pipeline_speedup = 0.0
    pipeline_sequential_ms = 0.0
    pipeline_pipelined_ms = 0.0
    if metadata:
        try:
            seq_fps = metadata.get('pipeline_sequential_fps', '0')
            if seq_fps and float(seq_fps) > 0:
                pipeline_sequential_fps = float(seq_fps)
                pipeline_pipelined_fps = float(metadata.get('pipeline_pipelined_fps', 0))
                pipeline_speedup = float(metadata.get('pipeline_speedup', 0))
                pipeline_sequential_ms = float(metadata.get('pipeline_sequential_ms', 0))
                pipeline_pipelined_ms = float(metadata.get('pipeline_pipelined_ms', 0))
        except (ValueError, TypeError):
            pass

    return KpiMetrics(
        # Latency (total E2E)
        latency_p50=latencies.quantile(0.50) if len(latencies) > 0 else 0,
        latency_p95=latencies.quantile(0.95) if len(latencies) > 0 else 0,
        latency_mean=latencies.mean() if len(latencies) > 0 else 0,
        latency_std=latencies.std() if len(latencies) > 0 else 0,
        latency_min=latencies.min() if len(latencies) > 0 else 0,
        latency_max=latencies.max() if len(latencies) > 0 else 0,
        inference_count=len(latencies),

        # E2E breakdown (P50)
        preprocess_p50=preprocess_p50,
        inference_only_p50=inference_only_p50,
        input_create_p50=input_create_p50,
        output_copy_p50=output_copy_p50,
        postprocess_p50=postprocess_p50,
        detection_count_mean=detection_count_mean,

        # Throughput
        fps=fps,

        # Thermal drift (E2E)
        first_30s_p50=first_30s_p50,
        last_30s_p50=last_30s_p50,
        drift_percent=drift_percent,

        # Inference-only drift
        infer_first_30s_p50=infer_first_30s_p50,
        infer_last_30s_p50=infer_last_30s_p50,
        infer_drift_percent=infer_drift_percent,

        # Thermal
        thermal_start=thermals.iloc[0] if len(thermals) > 0 else 0,
        thermal_end=thermals.iloc[-1] if len(thermals) > 0 else 0,
        thermal_max=thermals.max() if len(thermals) > 0 else 0,
        thermal_slope=thermal_slope,

        # Power
        power_mean=powers.mean() if len(powers) > 0 else 0,
        power_std=powers.std() if len(powers) > 0 else 0,

        # Memory
        memory_peak=int(memories.max()) if len(memories) > 0 else 0,
        memory_mean=memories.mean() if len(memories) > 0 else 0,
        memory_min=int(memories.min()) if len(memories) > 0 else 0,
        memory_start=int(memories.iloc[0]) if len(memories) > 0 else 0,
        memory_end=int(memories.iloc[-1]) if len(memories) > 0 else 0,

        # Duration
        duration_seconds=duration,

        # Cold start
        cold_start=cold_start,

        # ORT Profiling
        profiling_iterations=profiling_iterations,
        profiling_avg_total_ms=profiling_avg_total_ms,
        profiling_avg_npu_ms=profiling_avg_npu_ms,
        profiling_avg_cpu_ms=profiling_avg_cpu_ms,
        profiling_avg_fence_ms=profiling_avg_fence_ms,
        profiling_avg_overhead_ms=profiling_avg_overhead_ms,

        # Pipeline benchmark
        pipeline_sequential_fps=pipeline_sequential_fps,
        pipeline_pipelined_fps=pipeline_pipelined_fps,
        pipeline_speedup=pipeline_speedup,
        pipeline_sequential_ms=pipeline_sequential_ms,
        pipeline_pipelined_ms=pipeline_pipelined_ms
    )


def find_steady_state(
    df: pd.DataFrame,
    warm_up_seconds: float = 30.0
) -> pd.DataFrame:
    """
    Extract steady-state portion of the log (after warm-up period).

    Args:
        df: Full log DataFrame
        warm_up_seconds: Seconds to skip at the beginning

    Returns:
        DataFrame with only steady-state data
    """
    return df[df['elapsed_seconds'] >= warm_up_seconds].copy()


