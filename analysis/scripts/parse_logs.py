def load_all_logs(directory: str) -> pd.DataFrame:
    """
    Load all CSV logs from a directory and combine them.

    Args:
        directory: Path to directory containing CSV files

    Returns:
        Combined DataFrame with 'source_file' column
    """
    path = Path(directory)
    all_dfs = []

    for csv_file in path.glob("*.csv"):
        df = load_log(str(csv_file))
        df['source_file'] = csv_file.name
        all_dfs.append(df)

    if all_dfs:
        return pd.concat(all_dfs, ignore_index=True)
    else:
        return pd.DataFrame()


def generate_single_report(file_path: str) -> Tuple[str, dict, KpiMetrics]:
    """
    Generate detailed report string for a single log file.

    Args:
        file_path: Path to CSV file

    Returns:
        Tuple of (report_string, metadata, metrics)
    """
    lines = []
    lines.append(f"Loading: {file_path}")

    # Load metadata
    metadata = load_metadata(file_path)
    if metadata:
        lines.append("\n=== Metadata ===")
        lines.append(f"  Device: {metadata.get('device_manufacturer', 'N/A')} {metadata.get('device_model', 'N/A')}")
        lines.append(f"  SoC: {metadata.get('soc_model', 'N/A')}")
        lines.append(f"  EP: {metadata.get('execution_provider', 'N/A')}")
        lines.append(f"  Model: {metadata.get('model', 'N/A')}")

    df = load_log(file_path)

    lines.append(f"\nTotal records: {len(df)}")
    lines.append(f"Duration: {df['elapsed_seconds'].max():.1f} seconds")

    metrics = calculate_metrics(df, metadata)

    # Cold Start metrics
    if metrics.cold_start:
        lines.append("\n=== Cold Start ===")
        lines.append(f"  Model Load: {metrics.cold_start.model_load_ms:.0f} ms")
        lines.append(f"  Session Create (QNN compile): {metrics.cold_start.session_create_ms:.0f} ms")
        lines.append(f"  First Inference: {metrics.cold_start.first_inference_ms:.2f} ms")
        lines.append(f"  Total Cold: {metrics.cold_start.total_cold_ms:.0f} ms")

    # Graph Partitioning (from CSV metadata)
    total_nodes = int(metadata.get('ort_total_nodes', 0)) if metadata else 0
    if total_nodes > 0:
        qnn_nodes = int(metadata.get('ort_qnn_nodes', 0))
        cpu_nodes = int(metadata.get('ort_cpu_nodes', 0))
        coverage = metadata.get('ort_coverage_percent', 'N/A')
        fallback_ops = metadata.get('ort_fallback_ops', '')
        lines.append("\n=== Graph Partitioning ===")
        lines.append(f"  Total nodes: {total_nodes}")
        lines.append(f"  QNN nodes: {qnn_nodes}")
        lines.append(f"  CPU nodes: {cpu_nodes}")
        lines.append(f"  Coverage: {coverage}%")
        if fallback_ops:
            lines.append(f"  Fallback ops: {fallback_ops.replace(';', ', ')}")

    lines.append("\n=== Sustained Performance ===")
    lines.append(f"\nE2E Latency:")
    lines.append(f"  P50: {metrics.latency_p50:.2f} ms")
    lines.append(f"  P95: {metrics.latency_p95:.2f} ms")
    lines.append(f"  Mean: {metrics.latency_mean:.2f} ms (±{metrics.latency_std:.2f})")
    lines.append(f"  Min: {metrics.latency_min:.2f} ms")
    lines.append(f"  Max: {metrics.latency_max:.2f} ms")
    lines.append(f"  Count: {metrics.inference_count}")

    if metrics.inference_only_p50 > 0:
        lines.append(f"\nE2E Breakdown (P50):")
        lines.append(f"  Preprocess:     {metrics.preprocess_p50:.2f} ms")
        lines.append(f"  Input create:   {metrics.input_create_p50:.2f} ms")
        lines.append(f"  Inference:      {metrics.inference_only_p50:.2f} ms")
        lines.append(f"  Output copy:    {metrics.output_copy_p50:.2f} ms")
        lines.append(f"  Postprocess:    {metrics.postprocess_p50:.2f} ms")
        lines.append(f"  Avg Detections: {metrics.detection_count_mean:.1f}")

    if metrics.profiling_iterations > 0:
        lines.append(f"\nORT Profiling ({metrics.profiling_iterations} iterations):")
        lines.append(f"  session.run() avg:  {metrics.profiling_avg_total_ms:.2f} ms")
        lines.append(f"  NPU compute avg:    {metrics.profiling_avg_npu_ms:.2f} ms")
        lines.append(f"  CPU compute avg:    {metrics.profiling_avg_cpu_ms:.2f} ms")
        lines.append(f"  Fence (sync) avg:   {metrics.profiling_avg_fence_ms:.2f} ms")
        lines.append(f"  ORT overhead avg:   {metrics.profiling_avg_overhead_ms:.2f} ms")
        if metrics.profiling_avg_total_ms > 0:
            npu_ratio = metrics.profiling_avg_npu_ms / metrics.profiling_avg_total_ms * 100
            lines.append(f"  NPU ratio:          {npu_ratio:.1f}%")

    if metrics.pipeline_sequential_fps > 0:
        lines.append(f"\nPipeline Benchmark:")
        lines.append(f"  Sequential:  {metrics.pipeline_sequential_ms:.2f} ms/frame ({metrics.pipeline_sequential_fps:.1f} FPS)")
        lines.append(f"  Pipelined:   {metrics.pipeline_pipelined_ms:.2f} ms/frame ({metrics.pipeline_pipelined_fps:.1f} FPS)")
        lines.append(f"  Speedup:     {metrics.pipeline_speedup:.2f}x")

    lines.append(f"\nThroughput:")
    lines.append(f"  FPS: {metrics.fps:.2f} inf/s")

    lines.append(f"\nE2E Thermal Drift:")
    lines.append(f"  First 30s P50: {metrics.first_30s_p50:.2f} ms")
    lines.append(f"  Last 30s P50: {metrics.last_30s_p50:.2f} ms")
    lines.append(f"  Drift: {metrics.drift_percent:+.1f}%")

    if metrics.infer_first_30s_p50 > 0:
        lines.append(f"\nInference-Only Drift:")
        lines.append(f"  First 30s P50: {metrics.infer_first_30s_p50:.2f} ms")
        lines.append(f"  Last 30s P50: {metrics.infer_last_30s_p50:.2f} ms")
        lines.append(f"  Drift: {metrics.infer_drift_percent:+.1f}%")

    lines.append(f"\nThermal:")
    lines.append(f"  Start: {metrics.thermal_start:.1f} °C")
    lines.append(f"  End: {metrics.thermal_end:.1f} °C")
    lines.append(f"  Max: {metrics.thermal_max:.1f} °C")
    lines.append(f"  Slope: {metrics.thermal_slope:.2f} °C/min")

    lines.append(f"\nPower:")
    lines.append(f"  Mean: {metrics.power_mean:.1f} mW (±{metrics.power_std:.1f})")

    lines.append(f"\nMemory (VmRSS):")
    lines.append(f"  Start: {metrics.memory_start} MB")
    lines.append(f"  End: {metrics.memory_end} MB")
    lines.append(f"  Peak: {metrics.memory_peak} MB")
    lines.append(f"  Mean: {metrics.memory_mean:.1f} MB")
    lines.append(f"  Delta: {metrics.memory_end - metrics.memory_start:+d} MB")

    return "\n".join(lines), metadata, metrics


def print_single_report(file_path: str):
    """Print detailed report for a single log file."""
    report, metadata, metrics = generate_single_report(file_path)
    print(report)
    return metadata, metrics


def generate_comparison_table(file_paths: list) -> str:
    """
    Generate sectioned comparison report for multiple log files.

    Args:
        file_paths: List of paths to CSV files

    Returns:
        Comparison report as string with multiple sections
    """
    import re

    # Load all data first
    experiments = []
    for file_path in file_paths:
        metadata = load_metadata(file_path)
        df = load_log(file_path)
        metrics = calculate_metrics(df, metadata)
        ort_log = load_ort_log(file_path)  # Load ORT log if available

        # Generate label: {base_model}_{precision}_{ep}
        name = Path(file_path).stem
        model_raw = metadata.get('model', 'Unknown')
        # Extract base model name (strip quantization info like "INT8 (QDQ)")
        base_model = re.sub(r'\s+INT8.*$', '', model_raw)
        ep_raw = metadata.get('execution_provider', 'CPU')
        ep_short = {'QNN_NPU': 'NPU', 'QNN_GPU': 'GPU', 'CPU': 'CPU'}.get(ep_raw, ep_raw)
        precision = metadata.get('precision', 'FP32')
        qnn_opts = metadata.get('qnn_options', '')
        if 'fp16=1' in qnn_opts and precision == 'FP32':
            precision = 'FP16'
        # Compact precision: INT8_DYNAMIC -> INT8Dyn, INT8_QDQ -> INT8QDQ
        prec_short = precision.replace('_DYNAMIC', 'Dyn').replace('_', '')
        label = f"{base_model}_{prec_short}_{ep_short}"

        experiments.append({
            'label': label,
            'name': name,
            'ep': metadata.get('execution_provider', 'N/A'),
            'model': metadata.get('model', 'N/A'),
            'precision': prec_short,
            'metrics': metrics,
            'metadata': metadata,
            'ort_log': ort_log
        })

    # Sort: FP32 first, then FP16, then INT8 variants
    prec_order = {'FP32': 0, 'FP16': 1, 'INT8QDQ': 2, 'INT8Dyn': 3, 'INT8': 4}
    experiments.sort(key=lambda e: prec_order.get(e['precision'], 9))

    lines = []
    sep = "=" * 120

    # Title
    lines.append(sep)
    lines.append("KPI Comparison Report")
    lines.append(sep)

    # Section 1: Experiment Overview
    lines.append("\n[1] Experiment Overview")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'EP':<12} {'Precision':<12} {'Model':<25}")
    lines.append("-" * 120)
    for i, exp in enumerate(experiments, 1):
        meta = exp['metadata']
        # Determine precision from metadata
        precision = meta.get('precision', 'FP32')
        qnn_opts = meta.get('qnn_options', '')
        # Check if FP16 is enabled for NPU
        if 'fp16=1' in qnn_opts and precision == 'FP32':
            precision = 'FP16'
        lines.append(f"{i:<4} {exp['label']:<40} {exp['ep']:<12} {precision:<12} {exp['model']:<25}")
    lines.append("")
    lines.append("    EP: Execution Provider (QNN_NPU=Hexagon HTP, QNN_GPU=Adreno GPU, CPU=ARM CPU)")
    lines.append("    Precision: FP32/FP16=부동소수점, INT8=양자화 (QDQ=Static, Dynamic)")

    # Section 2: E2E Latency Performance
    lines.append(f"\n\n[2] E2E Latency Performance")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'E2E P50(ms)':<13} {'E2E P95(ms)':<13} {'E2E Mean(ms)':<14} {'MaxFPS':<10}")
    lines.append("-" * 110)
    for i, exp in enumerate(experiments, 1):
        m = exp['metrics']
        max_fps = 1000.0 / m.latency_p50 if m.latency_p50 > 0 else 0
        lines.append(f"{i:<4} {exp['label']:<40} {m.latency_p50:<13.2f} {m.latency_p95:<13.2f} "
                     f"{m.latency_mean:<14.2f} {max_fps:<10.1f}")
    lines.append("")
    lines.append("    E2E P50/P95: 전체 파이프라인(pre+infer+post) 50th/95th percentile latency (lower=better)")
    lines.append("    MaxFPS: Theoretical max throughput = 1000/E2E P50 (higher=better)")

    # Section 2b: E2E Breakdown
    has_breakdown_data = any(exp['metrics'].inference_only_p50 > 0 for exp in experiments)
    has_boundary_data = any(exp['metrics'].input_create_p50 > 0 for exp in experiments)
    if has_breakdown_data:
        lines.append(f"\n\n[2b] E2E Breakdown (P50)")
        lines.append("-" * 140)
        if has_boundary_data:
            lines.append(f"{'#':<4} {'Label':<35} {'PreProc':<9} {'InCreate':<9} {'Infer':<9} {'OutCopy':<9} {'PostProc':<9} {'Dets':<6} {'Total':<9}")
        else:
            lines.append(f"{'#':<4} {'Label':<35} {'PreProc':<9} {'Infer':<9} {'PostProc':<9} {'Dets':<6} {'Total':<9}")
        lines.append("-" * 140)
        for i, exp in enumerate(experiments, 1):
            m = exp['metrics']
            if has_boundary_data:
                lines.append(f"{i:<4} {exp['label']:<35} {m.preprocess_p50:<9.2f} {m.input_create_p50:<9.2f} "
                             f"{m.inference_only_p50:<9.2f} {m.output_copy_p50:<9.2f} "
                             f"{m.postprocess_p50:<9.2f} {m.detection_count_mean:<6.1f} {m.latency_p50:<9.2f}")
            else:
                lines.append(f"{i:<4} {exp['label']:<35} {m.preprocess_p50:<9.2f} "
                             f"{m.inference_only_p50:<9.2f} "
                             f"{m.postprocess_p50:<9.2f} {m.detection_count_mean:<6.1f} {m.latency_p50:<9.2f}")
        lines.append("")
        lines.append("    PreProc:  Image preprocessing (letterbox + normalize + CHW)")
        if has_boundary_data:
            lines.append("    InCreate: Input tensor creation (Java→Native JNI boundary)")
        lines.append("    Infer:    Model inference only (session.run)")
        if has_boundary_data:
            lines.append("    OutCopy:  Output tensor copy (Native→Java buffer copy)")
        lines.append("    PostProc: Confidence filter + NMS + coordinate transform")

    # Section 2c: ORT Profiling (NPU compute vs framework overhead)
    has_profiling_data = any(exp['metrics'].profiling_iterations > 0 for exp in experiments)
    if has_profiling_data:
        lines.append(f"\n\n[2c] ORT Profiling (session.run Breakdown)")
        lines.append("-" * 140)
        lines.append(f"{'#':<4} {'Label':<35} {'session.run':<12} {'NPU(ms)':<10} {'CPU(ms)':<10} {'Fence(ms)':<10} {'ORT(ms)':<10} {'NPU%':<8}")
        lines.append("-" * 140)
        for i, exp in enumerate(experiments, 1):
            m = exp['metrics']
            if m.profiling_iterations > 0:
                npu_pct = m.profiling_avg_npu_ms / m.profiling_avg_total_ms * 100 if m.profiling_avg_total_ms > 0 else 0
                lines.append(f"{i:<4} {exp['label']:<35} {m.profiling_avg_total_ms:<12.2f} {m.profiling_avg_npu_ms:<10.2f} "
                             f"{m.profiling_avg_cpu_ms:<10.2f} {m.profiling_avg_fence_ms:<10.2f} "
                             f"{m.profiling_avg_overhead_ms:<10.2f} {npu_pct:<8.1f}")
            else:
                lines.append(f"{i:<4} {exp['label']:<35} {'N/A':<12} {'N/A':<10} {'N/A':<10} {'N/A':<10} {'N/A':<10} {'N/A':<8}")
        lines.append("")
        lines.append("    session.run = NPU + CPU + Fence + ORT overhead")
        lines.append("    NPU:   QNN EP 실제 연산 시간 (HTP 커널 실행)")
        lines.append("    CPU:   CPU fallback 노드 실행 시간")
        lines.append("    Fence: EP 동기화 배리어 (HTP dispatch + completion wait)")
        lines.append("    ORT:   프레임워크 오버헤드 (그래프 스케줄링, 텐서 관리, JNI)")
        lines.append("    NPU%:  session.run() 중 실제 NPU 연산 비율")

    # Section 3: Cold Start Breakdown
    lines.append(f"\n\n[3] Cold Start Breakdown")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'Total(ms)':<12} {'Load(ms)':<12} {'Session(ms)':<12} {'1stInf(ms)':<12}")
    lines.append("-" * 120)
    for i, exp in enumerate(experiments, 1):
        m = exp['metrics']
        if m.cold_start:
            total = m.cold_start.total_cold_ms
            load = m.cold_start.model_load_ms
            session = m.cold_start.session_create_ms
            first = m.cold_start.first_inference_ms
            lines.append(f"{i:<4} {exp['label']:<40} {total:<12.0f} {load:<12.0f} {session:<12.0f} {first:<12.2f}")
        else:
            lines.append(f"{i:<4} {exp['label']:<40} {'N/A':<12} {'N/A':<12} {'N/A':<12} {'N/A':<12}")
    lines.append("")
    lines.append("    Total: 앱 시작부터 첫 추론 완료까지 = Load + Session + 1stInf")
    lines.append("    Load: ONNX 모델 파일 로드 시간")
    lines.append("    Session: ORT 세션 생성 시간 (QNN EP는 HTP 그래프 컴파일 포함)")
    lines.append("    1stInf: 첫 번째 추론 지연시간")

    # Section 4: Thermal Drift Analysis
    lines.append(f"\n\n[4] Thermal Drift Analysis (E2E Latency P50 변화)")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'First30s(ms)':<14} {'Last30s(ms)':<14} {'Drift%':<10} {'Verdict':<16}")
    lines.append("-" * 120)
    for i, exp in enumerate(experiments, 1):
        m = exp['metrics']
        first_30s = m.first_30s_p50
        last_30s = m.last_30s_p50
        drift_pct = ((last_30s - first_30s) / first_30s * 100) if first_30s > 0 else 0

        # Verdict based on drift
        if abs(drift_pct) < 3:
            verdict = "Stable"
        elif drift_pct > 10:
            verdict = "Throttling"
        elif drift_pct > 3:
            verdict = "Slight throttle"
        elif drift_pct < -3:
            verdict = "Warmup effect"
        else:
            verdict = "Normal"

        lines.append(f"{i:<4} {exp['label']:<40} {first_30s:<14.2f} {last_30s:<14.2f} {drift_pct:<+10.1f} {verdict:<16}")
    lines.append("")
    lines.append("    First30s/Last30s: 벤치마크 처음/마지막 30초 동안의 E2E Latency P50")
    lines.append("    Drift%: (Last30s - First30s) / First30s × 100")
    lines.append("    Verdict: Stable(<±3%), Slight throttle(3~10%), Throttling(>10%), Warmup effect(<-3%)")

    # Section 4b: Inference-Only Drift Analysis
    has_infer_drift = any(exp['metrics'].infer_first_30s_p50 > 0 for exp in experiments)
    if has_infer_drift:
        lines.append(f"\n\n[4b] Inference-Only Drift Analysis (session.run P50 변화)")
        lines.append("-" * 140)
        lines.append(f"{'#':<4} {'Label':<40} {'First30s(ms)':<14} {'Last30s(ms)':<14} {'Drift%':<10} {'Verdict':<16}")
        lines.append("-" * 140)
        for i, exp in enumerate(experiments, 1):
            m = exp['metrics']
            first_30s = m.infer_first_30s_p50
            last_30s = m.infer_last_30s_p50
            drift_pct = m.infer_drift_percent

            if abs(drift_pct) < 3:
                verdict = "Stable"
            elif drift_pct > 10:
                verdict = "Throttling"
            elif drift_pct > 3:
                verdict = "Slight throttle"
            elif drift_pct < -3:
                verdict = "Warmup effect"
            else:
                verdict = "Normal"

            lines.append(f"{i:<4} {exp['label']:<40} {first_30s:<14.2f} {last_30s:<14.2f} {drift_pct:<+10.1f} {verdict:<16}")
        lines.append("")
        lines.append("    First30s/Last30s: 벤치마크 처음/마지막 30초 동안의 Inference-only (session.run) P50")
        lines.append("    E2E Drift와 비교하여 inference 자체의 sustained 성능 변화를 분리 관찰")

    # Section 5: System Resources
    lines.append(f"\n\n[5] System Resources")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'Power(mW)':<12} {'Slope(°C/min)':<14} {'MemPeak(MB)':<12}")
    lines.append("-" * 120)
    for i, exp in enumerate(experiments, 1):
        m = exp['metrics']
        lines.append(f"{i:<4} {exp['label']:<40} {m.power_mean:<12.1f} {m.thermal_slope:<14.2f} {m.memory_peak:<12}")
    lines.append("")
    lines.append("    Power: 평균 전력 소비량 (mW)")
    lines.append("    Slope: 온도 상승률 (°C/min, lower=better)")
    lines.append("    MemPeak: 최대 메모리 사용량 (MB)")

    # Section 5b: Memory Analysis
    lines.append(f"\n\n[5b] Memory Analysis (VmRSS)")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'Start(MB)':<10} {'End(MB)':<10} {'Peak(MB)':<10} {'Mean(MB)':<10} {'Delta(MB)':<10} {'Verdict':<16}")
    lines.append("-" * 120)
    for i, exp in enumerate(experiments, 1):
        m = exp['metrics']
        delta = m.memory_end - m.memory_start
        if m.memory_start > 0 and abs(delta) / m.memory_start * 100 > 10:
            verdict = "Leak risk" if delta > 0 else "Shrinking"
        elif delta > 20:
            verdict = "Growing"
        elif delta < -20:
            verdict = "Shrinking"
        else:
            verdict = "Stable"
        lines.append(f"{i:<4} {exp['label']:<40} {m.memory_start:<10} {m.memory_end:<10} "
                     f"{m.memory_peak:<10} {m.memory_mean:<10.0f} {delta:<+10} {verdict:<16}")
    lines.append("")
    lines.append("    Start/End: 벤치마크 시작/종료 시점의 VmRSS")
    lines.append("    Delta: End - Start (양수=메모리 증가, 음수=감소)")
    lines.append("    Verdict: Stable(±20MB 이내), Growing/Shrinking(>20MB), Leak risk(>10% 증가)")

    # Section 6: Model Details
    lines.append(f"\n\n[6] Model Details")
    lines.append("-" * 120)
    lines.append(f"{'#':<4} {'Label':<40} {'Size(KB)':<12} {'Input Shape':<24} {'QNN Options':<30}")
    lines.append("-" * 120)
    for i, exp in enumerate(experiments, 1):
        meta = exp['metadata']
        size_kb = meta.get('model_size_kb', 'N/A')
        input_shape = meta.get('input_shape', 'N/A')
        qnn_opts = meta.get('qnn_options', 'N/A')
        lines.append(f"{i:<4} {exp['label']:<40} {size_kb:<12} {input_shape:<24} {qnn_opts:<30}")
